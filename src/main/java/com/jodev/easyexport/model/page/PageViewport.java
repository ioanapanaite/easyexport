package com.jodev.easyexport.model.page;

public class PageViewport {
    private int width;
    private int height;

    public PageViewport(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
